-- ============================================================
-- 二、疑点线索结果表
-- 对应实体：ClueResult.java  |  主键：MP 雪花算法 ASSIGN_ID
-- ============================================================

CREATE TABLE clue_result (
    id               NUMBER          NOT NULL PRIMARY KEY,     -- MP 雪花算法，避免 OceanBase RETURNING 参数位异常
    apply_code       VARCHAR2(64)    NOT NULL,                 -- 机构/申请编码
    clue_type        VARCHAR2(64),                             -- UNTENDERED / SPLIT_PURCHASE / COLLUSIVE_BID / CONFLICT_OF_INTEREST
    risk_level       VARCHAR2(16),                             -- HIGH / MEDIUM / LOW
    clue_title       VARCHAR2(512),                            -- 线索标题
    clue_detail      CLOB,                                     -- 线索详情（规则命中的具体数据描述）
    related_amount   NUMBER(18, 2),                            -- 涉及金额（元）
    related_supplier VARCHAR2(512),                            -- 涉及供应商名称
    rule_name        VARCHAR2(128),                            -- 命中的规则名称
    status           VARCHAR2(32)    DEFAULT 'PENDING',        -- PENDING / CONFIRMED / DISMISSED
    created_at       TIMESTAMP
);

COMMENT ON TABLE  clue_result IS '疑点线索结果表，由规则引擎写入，Agent 读取后进行智能分析';
COMMENT ON COLUMN clue_result.apply_code  IS '机构/申请编码，用于隔离不同机构的线索数据';
COMMENT ON COLUMN clue_result.clue_type   IS '线索类型：UNTENDERED/SPLIT_PURCHASE/COLLUSIVE_BID/CONFLICT_OF_INTEREST';
COMMENT ON COLUMN clue_result.status      IS '处理状态：PENDING=待处理，CONFIRMED=已确认，DISMISSED=已驳回';


-- ============================================================
-- 三、Agent 主任务表
-- 对应实体：AgentTask.java  |  主键：数据库自增 IDENTITY（IdType.AUTO）
-- ============================================================

CREATE TABLE agent_task (
    id              NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    task_name       VARCHAR2(256),                             -- 任务名称
    goal            CLOB,                                      -- 任务目标描述（用户输入的原始诉求）
    status          VARCHAR2(32),                              -- PENDING/RUNNING/REVIEWING/COMMUNICATING/COMPLETED/FAILED
    planner_result  CLOB,                                      -- Planner 输出的执行计划（JSON）
    executor_result CLOB,                                      -- Executor 执行结果汇总
    reviewer_score  NUMBER(3),                                 -- Reviewer 评分（0~100，低于阈值触发重试）
    final_report    CLOB,                                      -- Communicator 生成的最终报告
    created_at      TIMESTAMP,
    updated_at      TIMESTAMP
);

COMMENT ON TABLE  agent_task IS 'Agent 主任务表，记录完整任务生命周期';
COMMENT ON COLUMN agent_task.status         IS '任务状态：PENDING/RUNNING/REVIEWING/COMMUNICATING/COMPLETED/FAILED';
COMMENT ON COLUMN agent_task.planner_result IS 'Planner 输出的子任务计划，JSON 数组格式';
COMMENT ON COLUMN agent_task.reviewer_score IS 'Reviewer 评审评分 0~100，低于配置阈值触发重试';


-- ============================================================
-- 四、Agent 子任务表
-- 对应实体：AgentSubTask.java  |  主键：数据库自增 IDENTITY（IdType.AUTO）
